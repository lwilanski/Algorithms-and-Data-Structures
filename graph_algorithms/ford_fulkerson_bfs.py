from collections import deque


def bfs(G, s, t, parent):
    visited = [False] * len(G)
    queue = deque([s])
    visited[s] = True
    while queue:
        vertex = queue.popleft()
        for ind, val in enumerate(G[vertex]):
            if not visited[ind] and val:
                parent[ind] = vertex
                if ind == t:
                    return True

                visited[ind] = True
                queue.append(ind)

    return False


def ford_fulkerson(graph, source, sink):
    parent = [-1] * (len(graph))
    max_flow = 0
    while bfs(graph, source, sink, parent):
        path_flow = float("Inf")
        s = sink
        while s != source:
            path_flow = min(path_flow, graph[parent[s]][s])
            s = parent[s]
        max_flow += path_flow
        v = sink
        while v != source:
            u = parent[v]
            graph[u][v] -= path_flow
            graph[v][u] += path_flow
            v = parent[v]
    return max_flow


