def dfs(G, s, t, visited, parents):
    if s == t:
        return True

    visited[s] = True
    for neighbor in range(0, len(G)):
        if G[s][neighbor] > 0 and not visited[neighbor]:
            parents[neighbor] = s
            res = dfs(G, neighbor, t, visited, parents)
            if res:
                return res

    return False


def ford_fulkerson(G, source, sink):
    parents = [None] * len(G)
    max_flow = 0
    visited = [False] * len(G)

    while dfs(G, source, sink, visited, parents):
        path_flow = float('inf')
        current = sink

        while current != source:
            path_flow = min(path_flow, G[parents[current]][current])
            current = parents[current]

        max_flow += path_flow
        current = sink

        while current != source:
            G[parents[current]][current] -= path_flow
            G[current][parents[current]] += path_flow
            current = parents[current]

        visited = [False] * len(G)

    return max_flow


